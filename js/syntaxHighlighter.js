function loadHighlightJs() {
    document.querySelectorAll('pre code').forEach((block) => {
        hljs.highlightElement(block);
        setLanguageLabel(block);
    });
}

function setLanguageLabel(codeBlock) {
    const container = codeBlock.closest('.highlight-container');
    const label = container.querySelector('.highlight-label');
    const languageClass = codeBlock.className.split(' ').find(cls => cls.startsWith('language-'));
    if (languageClass) {
        const language = languageClass.replace('language-', '');
        label.textContent = language.charAt(0).toUpperCase() + language.slice(1);
    }
}

